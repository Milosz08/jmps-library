package org.jmpsl.gfx.sender;

import lombok.Builder;

import org.jmpsl.gfx.ImageExtension;
import org.jmpsl.gfx.IBufferedImagePayload;
import org.jmpsl.file.hashcode.FileHashCodeGenerator;

/**
 * Simple POJO record representing temporary file informations for generated file and save on external SFTP server.
 * Stored following parameters:
 *
 * <ul>
 *     <li><code>bytesRepresentation</code> - image as stream of array bytes</li>
 *     <li><code>uniqueImagePrefix</code> - user image prefix (avatar, banner etc.)</li>
 *     <li><code>id</code> - user id (from database)</li>
 *     <li><code>extensionName</code> - image extension string representation (ex. png, jpeg etc.)</li>
 *     <li><code>userHashCode</code> - user hash code (generated by {@link FileHashCodeGenerator}, from database)</li>
 * </ul>
 *
 * @author Mi≈Çosz Gilga
 * @since 1.0.2
 */
@Builder
public record TempImageSavePayload(
    byte[] bytesRepresentation, String uniqueImagePrefix, Long id, String extensionName, String userHashCode
) {
    public TempImageSavePayload(byte[] bytes, IBufferedImagePayload payload, ImageExtension extension) {
        this(bytes, payload.getImageUniquePrefix(), payload.getId(), payload.getUserHashCode(),
            extension.getImageExtension());
    }
}
